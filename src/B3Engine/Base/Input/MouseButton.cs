
namespace B3 {
	/// <summary>An enumeration of all the different mouse buttons</summary>
	public enum MouseButton : int {
		/// <summary>If the mouse button is unknown</summary>
		Unknown = -1,
		/// <summary>The Left mouse button (LMB)</summary>
		Left,
		/// <summary>The Middle mouse button (MMB)</summary>
		Middle,
		/// <summary>The Right mouse button (RMB)</summary>
		Right,
		/// <summary>The 4th button on the mouse</summary>
		X1,
		/// <summary>The 5th button on the mouse</summary>
		X2
	}
}
