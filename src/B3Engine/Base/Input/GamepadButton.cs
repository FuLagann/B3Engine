
namespace B3 {
	/// <summary>An enumartion for all the gamepad buttons the player can use</summary>
	public enum GamepadButton : int {
		/// <summary>If the gamepad button is unknown</summary>
		Unknown = -1,
		/// <summary>The A / X (Cross) button</summary>
		A,
		/// <summary>The B / O (Circle) button</summary>
		B,
		/// <summary>The X / Square button</summary>
		X,
		/// <summary>The Y / Triangle button</summary>
		Y,
		/// <summary>The Back / Share / Minus button</summary>
		Back,
		/// <summary>The Guide (Xbox / PS / Home) button</summary>
		Guide,
		/// <summary>The Start / Options / Plus button</summary>
		Start,
		/// <summary>The Left Stick (click) button</summary>
		LeftStick,
		/// <summary>The Right Stick (click) button</summary>
		RightStick,
		/// <summary>The Left Shoulder (LB / L1 / L) button</summary>
		LeftShoulder,
		/// <summary>The Right Shoulder (RB / R1 / R) button</summary>
		RightShoulder,
		/// <summary>The Up Dpad button</summary>
		DpadUp,
		/// <summary>The Down Dpad button</summary>
		DpadDown,
		/// <summary>The Left Dpad button</summary>
		DpadLeft,
		/// <summary>The Right Dpad button</summary>
		DpadRight
	}
}
