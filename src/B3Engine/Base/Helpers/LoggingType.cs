
namespace B3 {
	/// <summary>An enumeration for the different types of logging</summary>
	public enum LoggingType {
		/// <summary>For when the log is purely informational</summary>
		Info,
		/// <summary>For when the log is giving out a warning</summary>
		Warning,
		/// <summary>For when the log is giving out an error</summary>
		Error
	}
}
