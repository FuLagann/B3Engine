
namespace B3.Graphics {
	/// <summary>An enumeration for the different primitive data types a vertex attribute can be</summary>
	public enum VertexAttributeDataType {
		/// <summary>When the vertex attribute is a byte</summary>
		Byte = 5120,
		/// <summary>When the vertex attribute is an unsigned byte</summary>
		UByte = 5121,
		/// <summary>When the vertex attribute is a short</summary>
		Short = 5122,
		/// <summary>When the vertex attribute is an unsigned byte</summary>
		UShort = 5123,
		/// <summary>When the vertex attribute is an integer</summary>
		Int = 5124,
		/// <summary>When the vertex attribute is an unsigned integer</summary>
		UInt = 5125,
		/// <summary>When the vertex attribute is a float</summary>
		Float = 5126,
		/// <summary>When the vertex attribute is a double</summary>
		Double = 5130
	}
}
