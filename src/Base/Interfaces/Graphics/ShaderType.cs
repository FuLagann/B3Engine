
namespace B3.Graphics {
	/// <summary>An enumeration for all the different shader types that can be used</summary>
	public enum ShaderType {
		/// <summary>The shader type for vertex shaders</summary>
		Vertex,
		/// <summary>The shader type for fragment shaders</summary>
		Fragment,
		/// <summary>The shader type for geometry shaders</summary>
		Geometry,
		/// <summary>The shader type for tessellation control shaders</summary>
		TessellationControl,
		/// <summary>The shader type for tessellation evaluation shaders</summary>
		TessellationEvaluation,
		/// <summary>The shader type for compute shaders</summary>
		Compute
	}
}
